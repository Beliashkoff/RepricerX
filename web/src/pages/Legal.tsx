export default function LegalPage() {
  const { doc = 'terms' } = useParams()

  if (doc === 'archive') return <ArchivePage />

  const content = legalDocs[doc]
  if (!content) return <Navigate to="/legal/terms" replace />

  return (
    <PublicLayout>
      <article className="max-w-4xl mx-auto px-6 py-16">
        <p className="text-sm font-medium text-[#777] mb-3">Редакция от {content.updatedAt}</p>
        <h1 className="text-4xl font-bold text-[#111] mb-4">{content.title}</h1>
        <p className="text-lg text-[#666] mb-10">{content.summary}</p>

        <div className="space-y-8">
          {content.sections.map(section => (
            <section key={section.heading}>
              <h2 className="text-2xl font-semibold text-[#111] mb-3">{section.heading}</h2>
              <div className="space-y-3 text-[#555] leading-7">
                {section.body.map(paragraph => (
                  <p key={paragraph}>{paragraph}</p>
                ))}
              </div>
            </section>
          ))}
        </div>

        <div className="mt-12 rounded-2xl bg-[#f7f8fa] border border-[#e6e6e6] p-5 text-sm text-[#666]">
          Документ подготовлен для продукта RepricerX и должен быть дополнен реквизитами оператора перед коммерческой публикацией.
        </div>
      </article>
    </PublicLayout>
  )
}

